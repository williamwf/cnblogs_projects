# 版本Python 3.7
## 1. 原始数据类型和运算符

# 模除
7 % 3 # => 1

# x的y次方
2**4 # => 16

# 格式化字符串
"{} can be {}".format("strings", "interpolated")
#=> "strings can be interpolated"

# 重复参数
"{0} can be nimble, {0} be quick, {0} jump over the {1}".format("Jack", "candle stick")
#=> "Jack be nimble, Jack be quick, Jack jump over the candle stick"








